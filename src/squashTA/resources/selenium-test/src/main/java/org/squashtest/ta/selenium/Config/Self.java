package org.squashtest.ta.selenium.Config;

public class Self
{
    private String href;

    public String getHref ()
    {
        return href;
    }

    public void setHref (String href)
    {
        this.href = href;
    }

    @Override
    public String toString()
    {
        return "Self [href = "+href+"]";
    }
}
			
